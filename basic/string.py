# 슬라이싱 관련 

nums = "0123456789"

# 문자열[a:b] -> 
# a 인덱스 부터 시작해서
# b 의 이전 인덱스까지 추출
# 다음 케이스에서는 a 가 3 , b 가 6
# 즉 [3] ~ [5] 까지 추출
print(nums[3:6])
# [0] ~ [5] 까지 추출
print(nums[:6])

# [1] 에서 시작해 (기준으로) 매 3번째 요소들 추출
# [1] 추출 -> [1+3] -> [1+3+3] -> [1+3+3+3 ..]
print(nums[1::3])

# 홀수 추출
print(nums[1::2])

