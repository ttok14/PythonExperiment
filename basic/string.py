# 포맷팅 관련

name = "제이스"

# 정수임을 주의
height = 180

# "" 앞에 f (formatted string) 을 붙이면 
# 문자열 포맷팅이 가능하다란 의미이며 
# 이는 중괄호인 {} 을 사용하여 직접 변수나 표현식을
# 삽입할 수 있다란 의미  
print(f"안녕하세요 {name} 입니다.")

# 정수 변수를 사용하여 포맷팅
print(f"키는 {height-5} ~ {height+5} 입니다.")

result = 15.123

# {result:1} -> 최소 1자리
# {result:2} -> 최소 2자리
# .. 
# 다음은 최소 10 자리를 출력하라! 란 의미
# 10 자리 미만이면 스페이스로 채워진다
print(f"*최소* 10개를 채워라 ! :_{result:10}_")
# 다음은 최소 1개를 표시 및 소수점 2자리 까지 출력하라! 란 의미
print(f"{result:1.2f}")

# format() 함수를 이용한 포맷팅
# format() 에서 지정한 키워드 r 이 {r:1.2f} 에서 r 에 매칭
print("format() 포매팅 :|{r:1.2f}|".format(r=result))
