# 함수 활용

phone = "010-1234-5678"

# 문자열 개수 가져오기
print(len(phone))

# 문자 "-" 의 개수 가져오기  
print(phone.count("-"))

# 문자 "-" 을 기준으로 나눠서 리스트로 가져오기
print(phone.split("-"))
print("-".join(phone.split("-")))

# 문자 "-" 을 스페이스인 " " 으로 변경
print(phone.replace("-", " "))

